
var num = +prompt("Enter a number between 1 to 7 for day of week: ");

switch (num) {
    case 1: document.write("יום ראשון");
        break;
    case 2: document.write("יום שני");
        break;
    case 3: document.write("יום שלישי");
        break;
    case 4: document.write("יום רביעי");
        break;
    case 5: document.write("יום חמישי");
        break;
    case 6: document.write("יום שישי");
        break;
    case 7: document.write("יום שבת");
        break;
    default: document.write("שגיאה");
}